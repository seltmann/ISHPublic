<h1>ISH Distinguished Research Medal</h1>
<h2>Dr. Alexandr P. Rasnitsyn</h2>

 <p>On August 25, 2008, the fourth ISH Distinguished Research Medal was awarded to Alex Rasnitsyn in a ceremony at the Ocean Grill restaurant next to the American Museum of Natural History in New York City, USA.</p>
 <p>The award committee, consisting of  Drs. Denis Brothers, Gary Gibson, Norm Johnson, and Lars Vilhelmsen, wrote:	</p>
 <blockquote>We have the pleasure to select Prof. Dr. Alexandr Pavlovich Rasnitsyn for the ISH distinguished research medal. During a career spanning almost half a century, Dr. Rasnitsyn has published almost 150 papers and several books or book contributions dealing directly with different aspects of the systematics and evolution of Hymenoptera. Despite being well past average retirement age, he is still as active as ever with publications, travels and conferences. His research reaches across the entire order, including numerous papers on fossil taxa. His publications encompass systematics at all levels from species descriptions to higher-level phylogeny at the subordinal and superfamily level.</blockquote>
<p>It is within the field of higher-level phylogeny that the work of Dr. Rasnitsyn has had the largest impact on the community of hymenopteran systematists. Most members of the community have had to consider his ideas at one time or another. His papers from 1969 and 1980 (in Russian, later translated into English) and especially the 1988 summary in English are widely cited and stand as landmarks in the development of hymenopteran phylogenetics. These papers represent the cumulative effort of decades of original research that has allowed Dr. Rasnitsyn to acquire an unsurpassed knowledge of the diversity of Hymenoptera, living and extinct. Furthermore, his hypotheses surfaced at a time when the field was dominated by ideas derived from compilations of extensive literature surveys but with little original input, and his efforts managed to inspire and give new direction to investigations into hymenopteran phylogeny.</p>
 <p>For the wide and varied scope of his research, the profound impact of his ideas on hymenopteran phylogenetics, and his sheer industry, Alex can be considered &#145;the father of Hymenoptera phylogenetics&#146; and we consider him a highly deserving recipient of the highest honour the ISH can bestow on him.</p>
