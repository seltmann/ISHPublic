<p>The International Society of Hymenopterists publishes the Journal of Hymenoptera Research – an entomological journal dedicated to the study of hymenopterans (sawflies, bees, ants, and other wasps). Subject matter includes biology, behavior, ecology, genetics, morphology, systematics, and taxonomy. The journal is Open Access and all papers are free to read, download, print, and distribute. </p>  

<p>The Journal of Hymenoptera Research is published in four different digital formats: (a) print version, (b) PDF that is identical to the print version, (c) semantically enhanced HTML version to provide interactive reading and links to external resources, and (d) an XML version for archival purposes to facilitate future use and reuse of content. Membership to the society is important as the journal is financed by our membership and if we do not have enough members this valuable resource for Hymenoptera research will cease to exist! So even if you can get the journal in your library, you should join, to ensure that the journal gets published at all!</p>

<p>All manuscripts should be submitted through the online submission system of the journal. Instructions for authors and other information are available from the <a href="http://www.pensoft.net/journals/jhr/">publisher’s website</a>.</p>

<p>Every member paying dues receives the journal for free. Print copies are available for a cost.</p>

