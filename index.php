 <h1>Welcome</h1>
  <p>Welcome to the homepage of the <span style="font-weight: bold">International Society of Hymenopterists</span> (ISH). The aim of the Society is to encourage scientific research and promote the diffusion of knowledge about sawflies, bees, ants, and other wasps. Membership is open to all persons with an interest in hymenopterans. If you are not yet a member, please consider <a href="join.php">joining the Society</a>.  Memberships support our journals and our meetings, and as a member you will benefit from the large network of other Hymenoptera researchers, as well as the discounted page fees for the <span style="font-style: italic">Journal of Hymenoptera Research</span>.</p>
