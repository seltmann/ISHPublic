<h1>ISH Awards</h1>
<h2>Distinguished Research Medal</h2>

 <p>The Distinguished Research Medal is open to any 
biologist, and should go to an individual who has made significant and distinguished 
research contributions to the study of Hymenoptera. </p>
 <h4>Conditions:</h4><p>  The award will be made no more often than every second year. 
A candidate must be proposed by a Member in Good Standing and supported by two 
additional Members in Good Standing [one nomination is sufficient, which must be 
supported by a total of 3 Members in Good Standing]. 
 There is no restriction on candidates, beyond having made significant and distinguished 
research contributions to the study of Hymenoptera. 
 Proposers of the candidate shall provide the Secretary of the Society with the name, 
curriculum vitae (including qualifications, career, distinctions and publications), a 
statement of approximately 500 words explaining why the candidate merits the award, 
and the signatures of two additional sponsors who are Members in Good Standing of the 
Society. A selection committee for the award of the medal will be appointed and convened by the 
President, and members of that committee shall themselves not be nominated or be 
signatories of the nomination in that year. 
 Should the committee recommend to the Executive Committee that an award be made, 
the name of the recipient will be announced at the following annual meeting. 
 The recipient of the award will receive Life Membership in the Society, in addition to the 
medal.</p>
 <h4>Recipients:</h4>
 <ul>
   <li>John Noyes (2014)</li>
   <li>Bradleigh Vinson (2012)</li>
   <li>Charles Michener</li>
   <li>Zdenek Boucek</li>
