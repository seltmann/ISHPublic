  <h1>History of the Society</h1>
<p>The Society was organized in 1982 and incorporated in 1991. On December 2, 1982, at the Entomological Society of America meetings in Toronto, Canada, a large group of hymenopterists met to discuss formation of an organization devoted to the study of all aspects of the Hymenoptera. From that embryonic meeting emerged The International Society of Hymenopterists.</p>
