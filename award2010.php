 <h2> Service Award</h2>
 <p>The Service Award is meant to recognize individuals 
   who have made significant contributions in service to the Society that went well beyond normal 
   expectations.  Officers and former officers of the Society can be considered, but obviously the 
   award is not restricted to these individuals. </p>
 <h4>Conditions:</h4>
 <p>A candidate may be proposed by one or more Members in Good Standing. 
Any Member in Good Standing or previous Members of the Society are eligible for the 
award including current members of the Executive Committee. Proposers of the candidate shall provide the Secretary of the Society with the name and a 
statement of approximately 300 words explaining why the candidate merits the award. 
A selection committee for the award will be appointed and convened by the President, 
and members of the committee shall themselves not be nominated or be signatories of a 
nomination in that year. 
Should the committee recommend to the Executive Committee that an award be made, the name of the recipient will be announced at the following annual meeting. </p>
