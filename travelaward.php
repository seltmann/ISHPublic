 <h1>	International Society of Hymenopterists
	Travel Awards to 8th ISH Congress in Cusco, Peru, July 20-25, 2014</h1>

<p><strong>Call for Proposals</strong></p>

<p><font color="red">Due: February 27, 2014</font></p>

<p>The International Society of Hymenopterists invites student and postgraduate researchers (recently graduated or postdoctoral researchers) to submit applications for travel awards to the 8th congress in Peru from July 20-25, 2014. Funding for awards is limited, with a maximum award of $1500 plus a registration waiver ($240). Proposals will be competitively judged based on a consideration of the following factors: a) presentation of a poster or oral presentation (abstract and title included); b) one page resume; c) one page letter from supervisor, d) indication of any other potential funding source.</p>

<p>Travel Award Program Details:</p>

<p>
<ol>
<strong>Eligibility:</strong>

<li>current graduate and postgraduate students ; must be members in good standing of the International Hymenopterists Society; eligible applicants may not have received a previous ISH travel award (i.e., to ICE in Korea).</li>
<li>Provide a brief budget for the trip. Are other funding sources available?</li>
<li>Awards: Typical award amounts will range from $500 to $1500 plus a waiver of registration fees.</li></p>
</ol>
<ol>
<strong>Applications:</strong>

<li>Single spaced 12 point Times, 1 inch margins.</li>

<li>1 page that includes the title and abstract of the presentation (indicate if you plan a poster or oral presentation), a brief outline of the overall research being conducted, and a brief budget (<$1500).  Please be realistic about what you will need. Also, please state if you can still attend with a lesser award ($500-1000 plus a registration waiver).</li>

<li>1-2 page detailed biography with publications and presentations.</li>

<li>1 page statement from your Major Professor (or advisor) that indicates your status and a recommendation for this award.</li></p>
